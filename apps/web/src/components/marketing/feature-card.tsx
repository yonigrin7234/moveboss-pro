import { ReactNode } from 'react';

interface FeatureCardProps {
  icon: ReactNode;
  title: string;
  description: string;
  highlight?: boolean;
  badge?: string;
}

export function FeatureCard({ icon, title, description, highlight, badge }: FeatureCardProps) {
  return (
    <div
      className={`relative p-6 rounded-2xl border transition-colors ${
        highlight
          ? 'bg-gradient-to-b from-green-500/[0.08] to-transparent border-green-500/20'
          : 'bg-white/[0.02] border-white/[0.06] hover:bg-white/[0.04]'
      }`}
    >
      {badge && (
        <span className="absolute top-4 right-4 px-2 py-0.5 text-[10px] font-medium bg-green-500/10 text-green-400 rounded border border-green-500/20">
          {badge}
        </span>
      )}
      <div
        className={`w-10 h-10 rounded-xl flex items-center justify-center mb-4 ${
          highlight ? 'bg-green-500/10' : 'bg-sky-500/10'
        }`}
      >
        {icon}
      </div>
      <h3 className="text-lg font-semibold text-white mb-2">{title}</h3>
      <p className="text-sm text-white/50 leading-relaxed">{description}</p>
    </div>
  );
}

